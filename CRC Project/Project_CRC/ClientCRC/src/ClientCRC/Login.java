    private void jLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jLoginActionPerformed
       String username=User.getText();
       String password=String.valueOf(Pass.getPassword());
       if(username.equals("")){
            JOptionPane.showMessageDialog(this, "attenzione inserisci username", "errore", JOptionPane.ERROR_MESSAGE);
       }
       else{
           try{
               URL indirizzo = new URL("http://localhost:8080/soap/servlet/rpcrouter");
               Call chiamata=new Call();
               chiamata.setTargetObjectURI("urn:crc");
               chiamata.setMethodName("VerificaUser");
               chiamata.setEncodingStyleURI(Constants.NS_URI_SOAP_ENC);
               Vector parametri=new Vector();
               parametri.addElement(new Parameter("username", String.class,username,null));
               parametri.addElement(new Parameter("password", String.class,password,null));
               chiamata.setParams(parametri);
               System.out.println("SOAP call parameters:"+chiamata);
               try{

                   Response risposta = chiamata.invoke(indirizzo, "");
                   Parameter par = risposta.getReturnValue();
                   Object value = par.getValue();
                   String msg=String.valueOf(value);

                   if (msg.equals("A")){
                       admin = 1;
                       Administrator admini = new Administrator(username, admin);
                       admini.setVisible(true);
                       this.dispose();
                      }

                   if (msg.equals("U")){
                       System.out.println("ciao");
                       User utente = new User(username,admin);
                       utente.setVisible(true);
                       this.dispose();
                   }

               }
               catch(SOAPException e){
                   JOptionPane.showMessageDialog(this, "attenzione si Ã¨ verificato un errore", "errore", JOptionPane.ERROR_MESSAGE);
                   System.out.println("errore causato:("+e.getFaultCode()+"):"+e.getMessage());
               }
           }
           catch(MalformedURLException ex){
               JOptionPane.showMessageDialog(this, "attenzione nome utente o password errate", "errore", JOptionPane.ERROR_MESSAGE);
               System.out.println(ex.getMessage());
           }
       }
            // TODO add your handling code here:
    }//GEN-LAST:event_jLoginActionPerformed

